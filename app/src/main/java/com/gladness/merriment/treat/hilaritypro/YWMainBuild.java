	public void selectRandomSymbols(VWinner symbols[][]) {
		for (int i = 0; i < ZLSymbolStrings.COLS; i++) {
			int r = FRSelectorGradle.PRNG.nextInt(DISTRIBUTION_SIZE);

			for (int j = 0; j < ZLSymbolStrings.ROWS; j++) {
				int n = r - (ZLSymbolStrings.ROWS / 2) + j;

				if (n < 0) {
					symbols[i][j] = DISTRIBUTION[i][DISTRIBUTION_SIZE + n];
				} else if (n >= DISTRIBUTION_SIZE) {
					symbols[i][j] = DISTRIBUTION[i][n - DISTRIBUTION_SIZE];
				} else {
					symbols[i][j] = DISTRIBUTION[i][n];
				}
			}
		}
	}
